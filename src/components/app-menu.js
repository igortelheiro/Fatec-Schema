export default function AppMenu(props) {
  const [anchorEl, setAnchorEl] = React.useState(null);

  function handleClick(event) {
    setAnchorEl(event.currentTarget);
  }

  function handleClose() {
    setAnchorEl(null);
  }

  function handleClickOpt(page){
    props.mainApp.routePage(page)
    handleClose()
  }

  const appButton = {
    WebkitAppRegion: "no-drag"
  }
  const menuIcon = {
    marginLeft: 'auto'
  }

  return (
    <div>
      <Tooltip title="Menu principal">
          <IconButton color="inherit" size="small" style={ appButton  }
              onClick={handleClick}
              >
              <MenuButton width="28"/>
          </IconButton>
      </Tooltip>

      <Menu
        id="simple-menu"
        anchorEl={anchorEl}
        keepMounted
        style={{width: '1000px'}}
        open={Boolean(anchorEl)}
        onClose={handleClose}
      >
        
        <MenuItem onClick={() => {handleClickOpt(Welcome)}}>Bem-vindo <HomeIcon style={ menuIcon }/> </MenuItem>
        <MenuItem onClick={() => {handleClickOpt(Login)}}>Login <LoginIcon style={ menuIcon }/> </MenuItem>
        <MenuItem onClick={() => {handleClickOpt(Command)}}>Profile<ProfileIcon style={ menuIcon }/></MenuItem>
        <MenuItem onClick={() => {handleClickOpt(Rest)}}>Game <GameIcon style={ menuIcon }/></MenuItem>
        <MenuItem onClick={() => {handleClickOpt(FS_Inifile)}}>Ranking <RankIcon style={ menuIcon }/> </MenuItem>
        <Divider/>
        {/*<MenuItem onClick={() => {handleClickOpt(AppConfig)}}>Configurações <ConfigIcon style={ menuIcon }/></MenuItem>*/}
        <MenuItem onClick={() => {handleClickOpt(AppAbout)}}>Sobre... <InfoIcon style={ menuIcon }/></MenuItem>
        
      </Menu>
    </div>
  );
}

